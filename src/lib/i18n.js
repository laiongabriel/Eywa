const DICT = { pt: PT, en: EN }

export function getT(language) {
  const lang = language in DICT ? language : 'pt'
  return function t(key) {
    const val = DICT[lang][key]
    if (val !== undefined) return val
    return DICT.pt[key] ?? key
  }
}
